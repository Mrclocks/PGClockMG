function t(key) {
  const lang = state.lang || 'fa';
  const parts = key.split('.');
  const lookup = (dict) => {
    let v = dict;
    for (const p of parts) {
      v = v?.[p];
      if (v === undefined) return undefined;
    }
    return v;
  };
  return lookup(I18N[lang]) ?? lookup(I18N.fa) ?? lookup(I18N.en) ?? key;
}

function tr(obj, lang) {
  if (!obj) return '';
  if (typeof obj === 'string') return obj;
  if (Array.isArray(obj)) return obj;
  const L = lang || state.lang || 'fa';
  return obj[L] || obj.fa || obj.en || '';
}

function setLang(lang) {
  state.lang = lang;
  localStorage.setItem('pg-migrator-lang', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'fa' ? 'rtl' : 'ltr';
  const labels = { fa: 'FA', en: 'EN', ru: 'RU' };
  const current = document.getElementById('langCurrentLabel');
  if (current) current.textContent = labels[lang] || lang.toUpperCase();
  document.querySelectorAll('.lang-option').forEach((b) => {
    b.classList.toggle('is-active', b.dataset.lang === lang);
  });
  const accordion = document.getElementById('langAccordion');
  if (accordion) accordion.open = false;
  applyI18n();
  renderGlobalChecks();
  if (state.selectedPanel) renderPanelPrereqs(state.selectedPanel.id);
  if (state.selectedPanel?.id === 'marzban' && typeof renderMarzbanModes === 'function') renderMarzbanModes();
  if (state.currentStep === 1 && state.panels.length) renderPanels();
}

